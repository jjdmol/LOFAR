class Station(object):
    """
    Represents a real or virtual station. Position is a tuple of three floats
    representing ITRF x,y,z coordinates.
    """
    def __init__(self, name, partition, pset, inputs):
        self.name	= name
        self.partition	= partition
        self.pset	= pset
        self.inputs	= inputs
    def getName(self):
        return self.name

