void TDSProtocolWrite::sendrequest()
{
  static void*  buf = 0;
  static size_t size = 0;

  // skip update if the Clocks settings have not been modified
  if (RTC::RegisterState::MODIFIED != Cache::getInstance().getBack().getClocks().getState().get(getBoardId()))
  {
    setContinue(true);
    return;
  }

  EPATdsProtocolEvent tdsprotocol;

  if (Cache::getInstance().getBack().getClocks()()(getBoardId()) == 160) {

    tdsprotocol.hdr.set(MEPHeader::TDS_PROTOCOL_HDR, MEPHeader::DST_RSP, MEPHeader::WRITE, sizeof(tds_160MHz));
    tdsprotocol.protocol.setBuffer(tds_160MHz, sizeof(tds_160MHz));

  } else if (Cache::getInstance().getBack().getClocks()()(getBoardId()) == 200) {

    tdsprotocol.hdr.set(MEPHeader::TDS_PROTOCOL_HDR, MEPHeader::DST_RSP, MEPHeader::WRITE, sizeof(tds_200MHz));
    tdsprotocol.protocol.setBuffer(tds_200MHz, sizeof(tds_200MHz));

  } else {

    tdsprotocol.hdr.set(MEPHeader::TDS_PROTOCOL_HDR, MEPHeader::DST_RSP, MEPHeader::WRITE, sizeof(tds_off));
    tdsprotocol.protocol.setBuffer(tds_off, sizeof(tds_off));

  }
  
  m_hdr = tdsprotocol.hdr; // remember header to match with ack
  getBoardPort().send(tdsprotocol);
}

